public class Bit4 extends Bit {
    public Bit4() {
        super();
    }

    public Bit4(boolean[] bits) {
        super(bits);
    }

    public Bit4(String string) {
        super(string);
    }

    public Bit4(int number) {
        super(number);
    }

    @Override
    public int getBitLength() {
        return 4;
    }
}
